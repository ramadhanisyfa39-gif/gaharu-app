<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class PembelianDetail extends Model
{
    /*
    |--------------------------------------------------------------------------
    | TABLE
    |--------------------------------------------------------------------------
    */

    protected $table = 'pembelian_detail';

    /*
    |--------------------------------------------------------------------------
    | FILLABLE
    |--------------------------------------------------------------------------
    */

    protected $fillable = [

        'pembelian_id',

        'barang_id',

        'qty',

        /*
        |--------------------------------------------------------------------------
        | harga = TOTAL HARGA
        |--------------------------------------------------------------------------
        */

        'harga',

        /*
        |--------------------------------------------------------------------------
        | HARGA PER QTY
        |--------------------------------------------------------------------------
        */

        'harga_per_qty',

        /*
        |--------------------------------------------------------------------------
        | FIFO BATCH
        |--------------------------------------------------------------------------
        */

        'batch_number',
    ];

    /*
    |--------------------------------------------------------------------------
    | CASTS
    |--------------------------------------------------------------------------
    */

    protected $casts = [

        'qty'
            => 'decimal:2',

        'harga'
            => 'decimal:2',

        'harga_per_qty'
            => 'decimal:2',
    ];

    /*
    |--------------------------------------------------------------------------
    | TIMESTAMPS
    |--------------------------------------------------------------------------
    */

    public $timestamps = false;

    /*
    |--------------------------------------------------------------------------
    | RELATION
    |--------------------------------------------------------------------------
    */

    public function pembelian()
    {
        return $this->belongsTo(
            Pembelian::class,
            'pembelian_id'
        );
    }

    public function barang()
    {
        return $this->belongsTo(
            MasterBarang::class,
            'barang_id'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | FIFO BATCH
    |--------------------------------------------------------------------------
    */

    public function fifoBatch()
    {
        return $this->hasOne(
            StokGudangBatch::class,
            'pembelian_detail_id'
        );
    }
}